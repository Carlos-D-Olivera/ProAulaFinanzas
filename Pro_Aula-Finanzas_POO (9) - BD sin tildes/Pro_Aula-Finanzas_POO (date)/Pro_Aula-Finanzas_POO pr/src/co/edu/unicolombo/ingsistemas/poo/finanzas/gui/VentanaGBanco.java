    private void botonBusacarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBusacarActionPerformed
        String nit = campoNit.getText();
        if (nit == null || nit.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El nit del banco es necesario. ");
            return;
        }
        EntityManagerFactory conexionBd = conectar();
        BancoJpaController crudlBanco = new BancoJpaController(conexionBd);

        b = crudlBanco.findBanco(nit);
        if (b == null) {
            JOptionPane.showMessageDialog(this, "El banco con NIT" + nit + ", no existe.");
        }
        campoNombre.setText(b.getNombre());
        campoDireccion.setText(b.getDireccion());
        campoEmail.setText(b.getEmail());
        campoTelefono.setText(b.getTelefono());
        botonEditar.setEnabled(true);
        botonEliminar.setEnabled(true);
    }//GEN-LAST:event_botonBusacarActionPerformed

    private void botonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEditarActionPerformed
        int vali = JOptionPane.showConfirmDialog(this, "¿Esta seguro(a) que desea editar?", "Confirme", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (vali == JOptionPane.YES_OPTION) {
            String telefono = campoTelefono.getText();
            String direccion = campoDireccion.getText();
            String email = campoEmail.getText();
            String contraseña = String.valueOf(campoContraseña.getPassword());

            b.setTelefono(telefono);
            b.setDireccion(direccion);
            b.setEmail(email);
            b.setClave(contraseña);
            EntityManagerFactory conexionBD = conectar();
            BancoJpaController crudBanco = new BancoJpaController(conexionBD);
            try {
                crudBanco.edit(b);
                JOptionPane.showMessageDialog(this, "Editado con exito!!");
                limpiarCampos();
            } catch (NonexistentEntityException ex) {
                JOptionPane.showMessageDialog(this, "El banco no existe");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error al editar banco");
            }
        }


    }//GEN-LAST:event_botonEditarActionPerformed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        int vali = JOptionPane.showConfirmDialog(this, "¿Seguro(s) que desea "
                + "eliminar?", "Confirmación", JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
        if (vali == JOptionPane.YES_OPTION) {
            EntityManagerFactory conexionBD = conectar();
            BancoJpaController crudBanco = new BancoJpaController(conexionBD);
            try {
                crudBanco.destroy(b.getNit());
                limpiarCampos();
            } catch (NonexistentEntityException ex) {
                JOptionPane.showMessageDialog(this, "El banco no existe");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error al eliminar banco");
            }
        }

    }//GEN-LAST:event_botonEliminarActionPerformed

