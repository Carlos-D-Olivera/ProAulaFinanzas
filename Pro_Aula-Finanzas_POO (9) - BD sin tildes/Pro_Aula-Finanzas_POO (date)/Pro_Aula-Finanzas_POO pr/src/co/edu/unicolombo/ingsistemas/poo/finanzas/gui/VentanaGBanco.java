    public void limpiarCampos() {
        campoNit.setText("");
        campoNombre.setText("");
        campoEmail.setText("");
        campoTelefono.setText("");
        campoDireccion.setText("");
        campoContraseña.setText("");
        campoConfirmarContraseña.setText("");
        return;
    }
    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        int vali = JOptionPane.showConfirmDialog(this, "¿Esta seguro(a) que desea guardar?", "Confirme", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (vali == JOptionPane.YES_OPTION) {
            String nit = campoNit.getText();
            String Nombre = campoNombre.getText();
            String email = campoEmail.getText();
            String telefono = campoTelefono.getText();
            String direccion = campoDireccion.getText();
            String contraseña = String.valueOf(campoContraseña.getPassword());
            String confirmarContraseña = String.valueOf(campoConfirmarContraseña.getPassword());
            Banco b = new Banco();
            b.setNit(nit);
            b.setNombre(Nombre);
            b.setEmail(email);
            b.setTelefono(telefono);
            b.setDireccion(direccion);
            b.setClave(contraseña);
            EntityManagerFactory conexionBd = Persistence.createEntityManagerFactory("Pro_Aula-Finanzas_POOPU");
            BancoJpaController crudl = new BancoJpaController(conexionBd);
            try {
                crudl.create(b);
                int total = crudl.getBancoCount();
                String mensaje = "Banco guardado con exito!!.\n Total: " + total;
                JOptionPane.showMessageDialog(this, mensaje);
                limpiarCampos();
            } catch (PreexistingEntityException ex) {
                JOptionPane.showMessageDialog(this, "Existe un Banco con este nit: " + nit);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error al guardar el Banco. ");
            }
        }
    }//GEN-LAST:event_botonGuardarActionPerformed

